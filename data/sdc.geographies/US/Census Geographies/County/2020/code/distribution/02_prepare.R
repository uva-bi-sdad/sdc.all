# dataset creation code - dataset preparation (transformation, new variables, linkage, etc)

# Import file from original
us_geo_census_cb_2020_county <- sf::st_read("US/Census Geographies/County/2020/data/original/us_geo_census_cb_2020_counties.geojson")
us_geo_census_cb_2020_county <- sf::st_transform(us_geo_census_cb_2020_county, 4326)


# Assign geoid
us_geo_census_cb_2020_county$geoid <- us_geo_census_cb_2020_county$GEOID

# Assign region_type
us_geo_census_cb_2020_county$region_type <- "county"

# Assign region_name
us_geo_census_cb_2020_county$region_name <- stringr::str_to_title(us_geo_census_cb_2020_county$NAMELSAD)

# Assign year
us_geo_census_cb_2020_county$year <- "2020"

# measure, measure_type, and value need to be included in non-geo datasets

# Select final columns
final_dataset <- us_geo_census_cb_2020_county[, c("geoid", "region_name", "region_type", "year", "geometry")]

# Simplify the geography
final_dataset_simplified <- rmapshaper::ms_simplify(final_dataset, keep_shapes=TRUE)

# Export final dataset
sf::st_write(final_dataset_simplified, "US/Census Geographies/county/2020/data/distribution/us_geo_census_cb_2020_counties.geojson",
             delete_dsn = TRUE)

